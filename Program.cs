// # C# Veri Tipleri ve Açıklamaları

// -**bool * *: `true` veya `false` değerini tutar. Mantıksal (boolean) veri tipidir.
// - **byte**: 8 - bitlik tam sayı. 0 ile 255 arasındaki pozitif değerleri tutar.
// - **sbyte**: İşaretli 8-bitlik tam sayı. -128 ile 127 arasındaki değerleri tutar.
// - **char**: Tek bir Unicode karakteri temsil eder. 16-bitlik veri tipidir.
// - **decimal**: Yüksek doğruluk gerektiren ondalık sayılar için kullanılır. Finansal hesaplamalar için idealdir. 128-bitlik veri tipidir.
// - **double**: Çift duyarlıklı kayan noktalı sayı. Yaklaşık 15-16 basamaklı doğruluk sunar. 64-bitlik veri tipidir.
// - **float**: Tek duyarlıklı kayan noktalı sayı. Yaklaşık 7 basamaklı doğruluk sunar. 32-bitlik veri tipidir.
// - **int**: 32 - bitlik tam sayı. -2,147,483,648 ile 2,147,483,647 arasındaki değerleri tutar.
// - **uint**: İşaretsiz 32-bitlik tam sayı. 0 ile 4,294,967,295 arasındaki değerleri tutar.
// - **long**: 64 - bitlik tam sayı. Çok büyük tam sayıları tutar. -9,223,372,036,854,775,808 ile 9,223,372,036,854,775,807 arasındaki değerleri tutar.
// - **ulong**: İşaretsiz 64-bitlik tam sayı. 0 ile 18,446,744,073,709,551,615 arasındaki değerleri tutar.
// - **short**: 16 - bitlik tam sayı. -32,768 ile 32,767 arasındaki değerleri tutar.
// - **ushort**: İşaretsiz 16-bitlik tam sayı. 0 ile 65,535 arasındaki değerleri tutar.
// - **string**: Karakter dizisini temsil eder. Birden fazla karakterin birleşimi.
// - **object**: Tüm C# tiplerinin temel sınıfıdır. Herhangi bir veri tipini tutabilir.
// - **DateTime**: Tarih ve saat bilgisini tutar.
// - **TimeSpan**: İki zaman noktası arasındaki farkı temsil eder.
// - **Guid**: Global benzersiz tanımlayıcı (GUID) temsil eder. 128-bitlik benzersiz bir tanımlayıcıdır.
